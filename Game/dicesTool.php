/*
==============================================================================
ROLL A DICE
==============================================================================
*/


function roll() {

	$rand = rand(1,6);
    return $rand;

}



/*
==============================================================================
ROLL FOR CHARGE
==============================================================================
*/

function charge(){

	$firstDice = roll();
	$secondDice = roll();

	return $firstDice+$secondDice;

}

/*
==============================================================================
ROLL N DICES WITH THRESHOLD
==============================================================================
*/

function rollDices($nDices, $threshold){

	$success = 0;

	if($threshold>6){

		return $nDices;

	}

	for($i = 0; $i<$nDices; $i++){

		$roll = roll();
		if($roll >= $threshold){

			$success++;

		}

	}

	return $success;


}



